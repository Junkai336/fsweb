package A0925;

public class A0925_1 {
    public static void main(String[] args) {
       int var1 = 10;
       System.out.println(var1);
       
       int var2 = 20;
       System.out.println(var2);
       
       int var3 = 0x10; //hexa 16진수 - 16
       System.out.println(var3);

       int var4 = (int)20.7;
       System.out.println(var4);

       Double var44 = 20.7;
       System.out.println(var44);
       
    }
}



